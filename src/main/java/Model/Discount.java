package Model;

/**
 *
 * @author ADMIN
 */
public class Discount {
    private int value;
    private String code;

    public Discount(int value, String code) {
        this.value = value;
        this.code = code;
    }

    public int getValue() {
        return value;
    }

    public void setValue(int value) {
        this.value = value;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }
    
    
}
